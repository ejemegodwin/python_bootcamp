# Using len() with range()
languages = ["Python", "Go", "JavaScript"]

for i in range(len(languages)):
    print(i, languages[i])

fruits = ["Apple", "Banana", "Orange", "Mango"]

for fruit in fruits:
    print(fruit)

for i in range(len(fruits)):
    print(i, fruits[i])

numbers = [2, 4, 6]

for number in numbers:
    print(number * 2)